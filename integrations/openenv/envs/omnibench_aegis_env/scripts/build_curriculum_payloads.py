def _slugify(text: str) -> str:
    value = re.sub(r"[^a-zA-Z0-9]+", "_", str(text or "").strip().lower()).strip("_")
    return value or "item"


def _id_from_name(text: str) -> str:
    return re.sub(r"[^a-zA-Z0-9]+", "", str(text or "").strip()).lower()


FALLBACK_SCENARIO_IDS = {domain: _id_from_name(name) for domain, name in FALLBACK_SCENARIO_NAMES.items()}


def _normalize_name(value: str | None) -> str:
    if normalize_domain_name is not None:
        try:
            return str(normalize_domain_name(value))
        except Exception:
            pass
    if not value:
        return ""
    return str(value).strip().replace("-", "_").replace(" ", "_").replace(".", "_").lower()


def _canonical_domain(value: str | None) -> str:
    raw = str(value or "").strip()
    normalized = _normalize_name(raw)
    if not normalized:
        return "general"
    if resolve_domain_name is not None:
        try:
            return str(resolve_domain_name(raw))
        except Exception:
            pass
    return LEGACY_DOMAIN_ALIASES.get(normalized, normalized)


def _canonical_scenario_id(value: str | None, *, domain: str | None = None) -> str:
    raw = str(value or "").strip()
    if raw:
        compact = _id_from_name(raw)
        if compact in SCENARIO_ID_ALIASES:
            return SCENARIO_ID_ALIASES[compact]
    canonical_domain = _canonical_domain(domain)
    return FALLBACK_SCENARIO_IDS.get(canonical_domain, _id_from_name(raw or canonical_domain))


def _level_rank(level_name: str) -> int:
    for idx, item in enumerate(DEFAULT_LEVELS):
        if item.name == level_name:
            return idx
    return len(DEFAULT_LEVELS)


def _deepcopy_jsonable(value: Any) -> Any:
    return json.loads(json.dumps(value, ensure_ascii=False))


def _load_json(path: Path) -> Any:
    with path.open("r", encoding="utf-8") as fh:
        return json.load(fh)


def _dump_json(path: Path, payload: Any) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w", encoding="utf-8") as fh:
        json.dump(payload, fh, indent=2, ensure_ascii=False)
        fh.write("\n")


def _normalize_only(values: Sequence[str] | None) -> set[str]:
    return {_normalize_name(str(v)) for v in (values or []) if str(v).strip()}


def _dedupe_notes(*groups: Sequence[str]) -> list[str]:
    seen: set[str] = set()
    output: list[str] = []
    for group in groups:
        for item in group or []:
            text = str(item).strip()
            if text and text not in seen:
                seen.add(text)
                output.append(text)
    return output


def _iter_mappings(value: Any) -> Iterator[Mapping[str, Any]]:
    if isinstance(value, Mapping):
        yield value
        for nested in value.values():
            yield from _iter_mappings(nested)
    elif isinstance(value, Sequence) and not isinstance(value, (str, bytes, bytearray)):
        for nested in value:
            yield from _iter_mappings(nested)


def _coerce_action_plan(value: Any) -> list[dict[str, Any]]:
    if not isinstance(value, Sequence) or isinstance(value, (str, bytes, bytearray)):
        return []
    plan: list[dict[str, Any]] = []
    for item in value:
        if not isinstance(item, Mapping):
            continue
        if "name" in item:
            plan.append({"name": str(item.get("name") or ""), "args": dict(item.get("args") or {})})
        elif "action" in item:
            args = dict(item.get("args") or {})
            for key, val in item.items():
                if key not in {"action", "args"}:
                    args[key] = val
            plan.append({"name": str(item.get("action") or ""), "args": args})
    return [step for step in plan if step.get("name")]


def _extract_action_plan_from_fixture(fixture: Mapping[str, Any]) -> list[dict[str, Any]]:
    action_examples = fixture.get("action_examples")
    if isinstance(action_examples, Mapping):
        for key in ("canonical", "shorthand"):
            plan = _coerce_action_plan(action_examples.get(key))
            if plan:
                return plan
    plan = _coerce_action_plan(fixture.get("action_plan"))
    if plan:
        return plan
    episodes = fixture.get("episodes")
    if isinstance(episodes, Sequence) and not isinstance(episodes, (str, bytes, bytearray)):
        for episode in episodes:
            if isinstance(episode, Mapping):
                plan = _coerce_action_plan(episode.get("action_plan"))
                if plan:
                    return plan
    if "action" in fixture or "name" in fixture:
        return _coerce_action_plan([fixture])
    return []


def _make_spec(
    *,
    domain: str,
    scenario_id: str,
    scenario_name: str,
    category: str | None = None,
    track_label: str | None = None,
    source_url: str | None = None,
) -> ScenarioSpec:
    canonical_domain = _canonical_domain(domain)
    clean_name = str(scenario_name or FALLBACK_SCENARIO_NAMES.get(canonical_domain) or canonical_domain)
    clean_id = _canonical_scenario_id(scenario_id or clean_name, domain=canonical_domain)
    env_id = f"{DEFAULT_ENV_NAME}:{canonical_domain}.{clean_id}"
    mission_id = f"{clean_id}_{_slugify(canonical_domain)}_sample"
    return ScenarioSpec(
        domain=canonical_domain,
        scenario_id=clean_id,
        scenario_name=clean_name,
        category=str(category or canonical_domain),
        track_label=str(track_label or FALLBACK_TRACK_LABELS.get(canonical_domain) or canonical_domain),
        source_url=str(source_url or ""),
        fixture_candidates=FIXTURE_CANDIDATES_BY_DOMAIN.get(canonical_domain, (f"sample_actions_{canonical_domain}.json",)),
        env_id=env_id,
        mission_id=mission_id,
        max_steps=int(MAX_STEPS_BY_DOMAIN.get(canonical_domain, 5)),
        target_score=1,
        default_action_plan=DEFAULT_ACTION_PLAN_BY_DOMAIN.get(canonical_domain, ({"name": "advance", "args": {"value": 1}},)),
    )


def _fallback_specs() -> dict[str, ScenarioSpec]:
    return {
        domain: _make_spec(
            domain=domain,
            scenario_id=FALLBACK_SCENARIO_IDS[domain],
            scenario_name=scenario_name,
            category=domain,
            track_label=FALLBACK_TRACK_LABELS.get(domain, domain),
            source_url="",
        )
        for domain, scenario_name in FALLBACK_SCENARIO_NAMES.items()
    }


def _registry_specs() -> dict[str, ScenarioSpec]:
    if list_domains is None or get_domain_spec is None:
        if REGISTRY_IMPORT_ERROR:
            raise CurriculumBuildError(f"registry import failed: {REGISTRY_IMPORT_ERROR}")
        raise CurriculumBuildError("registry helpers are unavailable")

    specs: dict[str, ScenarioSpec] = {}
    for domain_key in list_domains():
        canonical_domain = _canonical_domain(str(domain_key))
        raw_spec = get_domain_spec(canonical_domain)
        scenario_name = str(getattr(raw_spec, "scenario_name", "") or FALLBACK_SCENARIO_NAMES.get(canonical_domain, canonical_domain))
        scenario_id = str(getattr(raw_spec, "scenario_id", "") or _id_from_name(scenario_name))
        specs[canonical_domain] = _make_spec(
            domain=canonical_domain,
            scenario_id=scenario_id,
            scenario_name=scenario_name,
            category=str(getattr(raw_spec, "category", "") or canonical_domain),
            track_label=str(getattr(raw_spec, "track_label", "") or FALLBACK_TRACK_LABELS.get(canonical_domain, canonical_domain)),
            source_url=str(getattr(raw_spec, "source_url", "") or ""),
        )
    return specs


def _load_specs(source: str) -> tuple[dict[str, ScenarioSpec], str, list[str]]:
    warnings: list[str] = []
    if source not in {"auto", "registry", "fallback"}:
        raise CurriculumBuildError("source must be one of: auto, registry, fallback")

    if source in {"auto", "registry"}:
        try:
            specs = _registry_specs()
            if len(specs) < EXPECTED_DOMAIN_COUNT:
                warnings.append(f"registry returned {len(specs)} domains; expected {EXPECTED_DOMAIN_COUNT}")
            return specs, "registry", warnings
        except Exception as exc:
            if source == "registry":
                raise
            warnings.append(f"registry unavailable; using fallback specs: {type(exc).__name__}: {exc}")
    return _fallback_specs(), "fallback", warnings


def _registry_validation_report(import_all: bool) -> Mapping[str, Any] | None:
    if validate_registry is None:
        return {"ok": False, "error": REGISTRY_IMPORT_ERROR or "validate_registry unavailable"}
    try:
        report = validate_registry(import_all=import_all)
    except TypeError:
        report = validate_registry()  # type: ignore[misc]
    except Exception as exc:
        return {"ok": False, "error": f"{type(exc).__name__}: {exc}"}
    return report if isinstance(report, Mapping) else {"ok": False, "error": "validate_registry returned a non-object"}


def _resolve_input_dir(explicit: str | None) -> Path:
    if explicit:
        path = Path(explicit).resolve()
        if not path.exists():
            raise CurriculumBuildError(f"input directory does not exist: {path}")
        return path
    for candidate in DEFAULT_INPUT_CANDIDATES:
        if candidate.exists():
            return candidate
    tried = ", ".join(str(p) for p in DEFAULT_INPUT_CANDIDATES)
    raise CurriculumBuildError(f"could not locate generated payload directory; tried: {tried}")


def _load_aggregate_payloads(input_dir: Path) -> tuple[list[dict[str, Any]], list[dict[str, Any]]]:
    client_path = input_dir / "all_client_bundles.json"
    eval_path = input_dir / "all_openenv_eval_payloads.json"
    if not client_path.exists():
        raise CurriculumBuildError(f"missing aggregate client bundle file: {client_path}")
    if not eval_path.exists():
        raise CurriculumBuildError(f"missing aggregate eval payload file: {eval_path}")

    client_payloads = _load_json(client_path)
    eval_payloads = _load_json(eval_path)
    if not isinstance(client_payloads, list):
        raise CurriculumBuildError("all_client_bundles.json must be a JSON list")
    if not isinstance(eval_payloads, list):
        raise CurriculumBuildError("all_openenv_eval_payloads.json must be a JSON list")
    return [dict(item) for item in client_payloads if isinstance(item, Mapping)], [dict(item) for item in eval_payloads if isinstance(item, Mapping)]


def _payload_domain(payload: Mapping[str, Any]) -> str:
    domain = payload.get("domain") or (payload.get("reset_payload") or {}).get("options", {}).get("domain")
    return _canonical_domain(str(domain or "general"))


def _payload_scenario_id(payload: Mapping[str, Any], *, domain: str) -> str:
    scenario_id = payload.get("scenario_id") or (payload.get("reset_payload") or {}).get("scenario_id") or payload.get("scenario_name")
    return _canonical_scenario_id(str(scenario_id or ""), domain=domain)


def _make_index(items: Iterable[Mapping[str, Any]]) -> dict[tuple[str, str], dict[str, Any]]:
    index: dict[tuple[str, str], dict[str, Any]] = {}
    for item in items:
        domain = _payload_domain(item)
        scenario_id = _payload_scenario_id(item, domain=domain)
        if domain and scenario_id:
            index[(domain, scenario_id)] = dict(item)
    return index


def _resolve_spec_for_payload(
    *,
    domain: str,
    scenario_id: str,
    specs_by_domain: Mapping[str, ScenarioSpec],
    base_payload: Mapping[str, Any],
) -> ScenarioSpec:
    spec = specs_by_domain.get(domain)
    if spec:
        return spec
    scenario_name = str(base_payload.get("scenario_name") or scenario_id or FALLBACK_SCENARIO_NAMES.get(domain, domain))
    category = str(base_payload.get("category") or (base_payload.get("reset_payload") or {}).get("options", {}).get("category") or domain)
    return _make_spec(
        domain=domain,
        scenario_id=scenario_id,
        scenario_name=scenario_name,
        category=category,
        track_label=str(base_payload.get("track_label") or FALLBACK_TRACK_LABELS.get(domain, domain)),
        source_url=str(base_payload.get("source_url") or ""),
    )


def _resolve_fixture_path(spec: ScenarioSpec, input_dir: Path, base_payload: Mapping[str, Any]) -> Path | None:
    candidates: list[str] = []
    for value in (base_payload.get("fixture"), *(base_payload.get("fixture_candidates") or []), *spec.fixture_candidates):
        name = str(value or "").strip()
        if name and name not in candidates:
            candidates.append(name)
    search_dirs = [input_dir, *FIXTURE_SEARCH_DIRS]
    seen_dirs: set[str] = set()
    for directory in search_dirs:
        directory = Path(directory).resolve()
        key = str(directory)
        if key in seen_dirs:
            continue
        seen_dirs.add(key)
        for name in candidates:
            path = directory / name
            if path.exists():
                return path
    return None


def _load_fixture_override(spec: ScenarioSpec, input_dir: Path, base_payload: Mapping[str, Any]) -> tuple[dict[str, Any], str | None]:
    path = _resolve_fixture_path(spec, input_dir, base_payload)
    if path is None:
        return {}, None
    payload = _load_json(path)
    return (dict(payload), path.name) if isinstance(payload, Mapping) else ({}, path.name)


def _find_steps_by_name(payload: Mapping[str, Any], step_name: str) -> list[dict[str, Any]]:
    return [step for step in _coerce_action_plan(payload.get("action_plan")) if step.get("name") == step_name]


def _normalize_scalar(value: Any) -> Any:
    if isinstance(value, (str, int, float, bool)) or value is None:
        return value
    if isinstance(value, Mapping):
        return {str(k): _normalize_scalar(v) for k, v in value.items()}
    if isinstance(value, Sequence) and not isinstance(value, (str, bytes, bytearray)):
        return [_normalize_scalar(v) for v in value]
    return str(value)


def _seed_matches(candidate: Any, seed: int) -> bool:
    try:
        return int(candidate) == int(seed)
    except (TypeError, ValueError):
        return str(candidate).strip() == str(seed)


def _find_business_answer_maps(obj: Any, seed: int) -> list[dict[str, Any]]:
    matches: list[dict[str, Any]] = []
    required = {"route_to", "action", "reason_code", "privacy_safe"}
    for mapping in _iter_mappings(obj):
        candidate: Mapping[str, Any] | None = None
        if str(seed) in mapping and isinstance(mapping.get(str(seed)), Mapping):
            nested = dict(mapping[str(seed)])
            candidate = dict(nested.get("answer")) if isinstance(nested.get("answer"), Mapping) else nested
        elif mapping.get("seed") is not None and _seed_matches(mapping.get("seed"), seed):
            candidate = dict(mapping.get("answer")) if isinstance(mapping.get("answer"), Mapping) else mapping
        elif isinstance(mapping.get("answer"), Mapping):
            candidate = dict(mapping["answer"])
        elif required.issubset(mapping.keys()):
            candidate = mapping
        if candidate and required.issubset(candidate.keys()):
            matches.append({k: _normalize_scalar(candidate[k]) for k in ("route_to", "action", "reason_code", "privacy_safe")})
    return matches


def _derive_business_process_answer(
    *,
    seed: int,
    fixture_payload: Mapping[str, Any],
    base_payload: Mapping[str, Any],
    current_step_args: Mapping[str, Any],
) -> tuple[dict[str, Any] | None, str | None]:
    current_answer = current_step_args.get("answer") if isinstance(current_step_args, Mapping) else None
    if isinstance(current_answer, Mapping):
        for answer in _find_business_answer_maps({"answer": current_answer}, seed):
            return answer, None
    for source_name, source in (("fixture", fixture_payload), ("base", base_payload)):
        for step in _find_steps_by_name(source, "submit_answer"):
            for answer in _find_business_answer_maps({"answer": dict(step.get("args") or {}).get("answer")}, seed):
                return answer, f"submit_answer carried over from {source_name} canonical step"
    for source_name, source in (("fixture", fixture_payload), ("base", base_payload)):
        answers = _find_business_answer_maps(source, seed)
        if answers:
            return answers[0], f"submit_answer derived from {source_name} answer data"
    return None, None


_ID_KEYS = ("task_id", "id", "name", "slug", "key")
_TASK_CONTAINER_KEYS = {"tasks", "task_bundle", "bundle", "episodes", "assignments", "items"}


def _looks_like_task_id(text: Any) -> bool:
    if not isinstance(text, str):
        return False
    value = text.strip()
    if not value:
        return False
    if value.startswith("tt_"):
        return True
    return (bool(re.match(r"^[A-Za-z]+(?:_[A-Za-z0-9]+){1,}$", value)) and "task" in value.lower()) or value.lower().startswith("tt")


def _collect_task_ids(value: Any) -> list[str]:
    found: list[str] = []
    seen: set[str] = set()

    def add(candidate: Any) -> None:
        if isinstance(candidate, str):
            task_id = candidate.strip()
            if task_id and _looks_like_task_id(task_id) and task_id not in seen:
                seen.add(task_id)
                found.append(task_id)

    def walk(obj: Any, parent_key: str | None = None) -> None:
        if isinstance(obj, Mapping):
            for key in _ID_KEYS:
                add(obj.get(key))
            for key, nested in obj.items():
                if isinstance(nested, Mapping) and key == "task_rewards":
                    for reward_key in nested.keys():
                        add(reward_key)
                elif key in _TASK_CONTAINER_KEYS and isinstance(nested, Sequence) and not isinstance(nested, (str, bytes, bytearray)):
                    for item in nested:
                        if isinstance(item, Mapping):
                            for candidate_key in _ID_KEYS:
                                add(item.get(candidate_key))
                        walk(item, key)
                else:
                    walk(nested, str(key))
        elif isinstance(obj, Sequence) and not isinstance(obj, (str, bytes, bytearray)):
            for item in obj:
                walk(item, parent_key)
        elif parent_key in _ID_KEYS:
            add(obj)

    walk(value)
    return found


def _derive_tau2_task_rewards(
    *,
    fixture_payload: Mapping[str, Any],
    base_payload: Mapping[str, Any],
    current_step_args: Mapping[str, Any],
) -> tuple[dict[str, float] | None, str | None]:
    explicit_rewards = current_step_args.get("task_rewards") if isinstance(current_step_args, Mapping) else None
    if isinstance(explicit_rewards, Mapping) and explicit_rewards and set(explicit_rewards.keys()) != {"task_1"}:
        return ({str(k): float(v) for k, v in explicit_rewards.items()}, None)

    for source_name, source in (("fixture", fixture_payload), ("base", base_payload)):
        for step in _find_steps_by_name(source, "score_task_bundle"):
            rewards = dict(step.get("args") or {}).get("task_rewards")
            if isinstance(rewards, Mapping) and rewards and set(rewards.keys()) != {"task_1"}:
                return ({str(k): float(v) for k, v in rewards.items()}, f"task_rewards derived from {source_name} score_task_bundle step")

    for source_name, source in (("fixture", fixture_payload), ("base", base_payload)):
        task_ids = _collect_task_ids(source)
        if task_ids:
            return ({task_id: 1.0 for task_id in task_ids}, f"task_rewards derived from {source_name} task ids")

    if isinstance(explicit_rewards, Mapping) and explicit_rewards:
        return ({str(k): float(v) for k, v in explicit_rewards.items()}, None)
    return None, None


def _filter_notes_for_seed(notes: Sequence[str], *, seed: int) -> list[str]:
    filtered: list[str] = []
    for note in notes:
        text = str(note).strip()
        if not text:
            continue
        if "Seed 42" in text and int(seed) != 42:
            continue
        filtered.append(text)
    return filtered


def _materialize_scenario_action_plan(
    *,
    domain: str,
    scenario_id: str,
    seed: int,
    action_plan: Sequence[Mapping[str, Any]],
    fixture_payload: Mapping[str, Any],
    base_payload: Mapping[str, Any],
) -> tuple[list[dict[str, Any]], list[str]]:
    normalized = _coerce_action_plan(action_plan)
    notes: list[str] = []

    if domain == "business_process" and scenario_id == "saleforceonespy":
        updated: list[dict[str, Any]] = []
        for step in normalized:
            if step.get("name") != "submit_answer":
                updated.append(step)
                continue
            args = dict(step.get("args") or {})
            answer, note = _derive_business_process_answer(
                seed=seed,
                fixture_payload=fixture_payload,
                base_payload=base_payload,
                current_step_args=args,
            )
            if answer is not None:
                args["answer"] = answer
            updated.append({"name": step["name"], "args": args})
            if note:
                notes.append(note)
        return updated, notes

    if domain == "tau2" and scenario_id == "tickettwister":
        updated = []
        for step in normalized:
            if step.get("name") != "score_task_bundle":
                updated.append(step)
                continue
            args = dict(step.get("args") or {})
            task_rewards, note = _derive_tau2_task_rewards(
                fixture_payload=fixture_payload,
                base_payload=base_payload,
                current_step_args=args,
            )
            if task_rewards:
                args["task_rewards"] = task_rewards
            updated.append({"name": step["name"], "args": args})
            if note:
                notes.append(note)
        return updated, notes

    return normalized, notes


def _resolve_action_plan(
    *,
    spec: ScenarioSpec,
    fixture_payload: Mapping[str, Any],
    base_payload: Mapping[str, Any],
) -> tuple[list[dict[str, Any]], str]:
    plan = _extract_action_plan_from_fixture(fixture_payload)
    if plan:
        return plan, "fixture"
    plan = _coerce_action_plan(base_payload.get("action_plan"))
    if plan:
        return plan, "base_payload"
    plan = _coerce_action_plan(spec.default_action_plan)
    if plan:
        return plan, "default_spec"
    raise CurriculumBuildError(f"could not resolve action plan for {spec.domain}/{spec.scenario_id}")


def _build_canonical_seed_payload(
    base: Mapping[str, Any],
    *,
    spec: ScenarioSpec,
    fixture_payload: Mapping[str, Any],
    fixture_name: str | None,
    level: LevelSpec,
    payload_kind: str,
) -> dict[str, Any]:
    payload = deepcopy(dict(base))

    base_reset = dict(payload.get("reset_payload") or {})
    fixture_reset = dict(fixture_payload.get("reset_payload") or {})
    reset_payload = {**base_reset, **fixture_reset}

    base_options = dict(base_reset.get("options") or {})
    fixture_options = dict(fixture_reset.get("options") or {})
    options = {**base_options, **fixture_options}

    original_seed = int(reset_payload.get("seed") or payload.get("seed") or fixture_payload.get("seed") or 42)
    new_seed = original_seed + level.seed_offset

    mission_stub = str(
        fixture_payload.get("mission_id")
        or reset_payload.get("mission_id")
        or spec.mission_id
        or f"{spec.scenario_id}_{_slugify(spec.domain)}_sample"
    )
    if mission_stub.endswith("_sample") or mission_stub == spec.mission_id:
        mission_id = f"{spec.scenario_id}_{_slugify(spec.domain)}_{level.name}_seed{new_seed}"
    else:
        mission_id = f"{_slugify(mission_stub)}_{level.name}_seed{new_seed}"

    max_steps = int(options.get("max_steps") or spec.max_steps or 5)
    target_score = options.get("target_score") if options.get("target_score") is not None else spec.target_score

    options["env_id"] = spec.env_id
    options["domain"] = spec.domain
    options["category"] = spec.category
    options["scenario_name"] = spec.scenario_name
    options["source_url"] = spec.source_url
    options["max_steps"] = max(1, max_steps + level.max_steps_delta)
    options["target_score"] = target_score
    options["difficulty"] = level.name
    options["curriculum_level"] = level.name
    options["seed_variant"] = new_seed

    reset_payload["seed"] = new_seed
    reset_payload["scenario_id"] = spec.scenario_id
    reset_payload["scenario_name"] = spec.scenario_name
    reset_payload["mission_id"] = mission_id
    reset_payload["options"] = options

    action_plan, action_plan_source = _resolve_action_plan(
        spec=spec,
        fixture_payload=fixture_payload,
        base_payload=payload,
    )
    action_plan, scenario_notes = _materialize_scenario_action_plan(
        domain=spec.domain,
        scenario_id=spec.scenario_id,
        seed=new_seed,
        action_plan=action_plan,
        fixture_payload=fixture_payload,
        base_payload=payload,
    )

    notes = _dedupe_notes(
        _filter_notes_for_seed(payload.get("notes") or [], seed=new_seed),
        _filter_notes_for_seed(fixture_payload.get("notes") or [], seed=new_seed),
        scenario_notes,
        [
            f"Curriculum payload realigned for {spec.domain}/{spec.scenario_name} ({spec.scenario_id}).",
            f"action_plan_source={action_plan_source}",
        ],
    )

    payload["kind"] = payload.get("kind") or payload_kind
    payload["domain"] = spec.domain
    payload["category"] = spec.category
    payload["scenario_id"] = spec.scenario_id
    payload["scenario_name"] = spec.scenario_name
    payload["track_label"] = spec.track_label
    payload["source_url"] = spec.source_url
    payload["seed"] = new_seed
    payload["reset_payload"] = reset_payload
    payload["mission_id"] = mission_id
    payload["action_plan"] = [_deepcopy_jsonable(item) for item in action_plan]
    payload["action_plan_source"] = action_plan_source
    payload["curriculum_level"] = level.name
    payload["curriculum_rank"] = _level_rank(level.name)
    payload["curriculum_note"] = level.note
    payload["variant_key"] = f"{_slugify(spec.domain)}__{_slugify(spec.scenario_id)}__{level.name}"
    payload["curriculum_realigned"] = True
    payload["canonical_env_id"] = spec.env_id
    payload["canonical_fixture_candidates"] = list(spec.fixture_candidates)
    payload["notes"] = notes

    if fixture_name:
        payload["fixture"] = fixture_name
    elif payload.get("fixture"):
        payload["fixture"] = str(payload.get("fixture"))
    elif spec.fixture_candidates:
        payload["fixture"] = spec.fixture_candidates[0]

    # Keep OpenEnv eval payload fields explicit when the base object came from
    # all_openenv_eval_payloads.json.
    if payload_kind == "openenv_eval":
        payload["adapter"] = payload.get("adapter") or "openenv"
        payload["env_name"] = payload.get("env_name") or DEFAULT_ENV_NAME
        payload["live_check"] = bool(payload.get("live_check", True))
        payload["require_success"] = bool(payload.get("require_success", False))

    return payload


def _passes_only_filter(domain: str, scenario_id: str, scenario_name: str, only_set: set[str]) -> bool:
    if not only_set:
        return True
    candidates = {
        _normalize_name(domain),
        _normalize_name(scenario_id),
        _normalize_name(scenario_name),
        _normalize_name(_id_from_name(scenario_name)),
    }
    return bool(candidates & only_set)


def build_curriculum_payloads(
    *,
    input_dir: Path,
    output_dir: Path,
    levels: Sequence[str] | None = None,
    only: Sequence[str] | None = None,
    source: str = "auto",
    validate_registry_imports: bool = False,
) -> dict[str, Any]:
    requested_levels = {str(name).strip() for name in (levels or []) if str(name).strip()}
    chosen_levels = [spec for spec in DEFAULT_LEVELS if not requested_levels or spec.name in requested_levels]
    if not chosen_levels:
        raise CurriculumBuildError("no curriculum levels matched the requested filter")

    specs_by_domain, spec_source, warnings = _load_specs(source)
    registry_report = _registry_validation_report(import_all=validate_registry_imports) if source in {"auto", "registry"} else None

    client_payloads, eval_payloads = _load_aggregate_payloads(input_dir)
    client_index = _make_index(client_payloads)
    eval_index = _make_index(eval_payloads)
    all_keys = sorted(set(client_index) | set(eval_index))
    only_set = _normalize_only(only)

    output_dir.mkdir(parents=True, exist_ok=True)
    curriculum_clients: list[dict[str, Any]] = []
    curriculum_evals: list[dict[str, Any]] = []
    written_files: list[str] = []
    realigned_count = 0
    skipped: list[dict[str, str]] = []

    for domain, scenario_id in all_keys:
        base_client = client_index.get((domain, scenario_id), {})
        base_eval = eval_index.get((domain, scenario_id), {})
        base_for_spec = base_client or base_eval
        spec = _resolve_spec_for_payload(
            domain=domain,
            scenario_id=scenario_id,
            specs_by_domain=specs_by_domain,
            base_payload=base_for_spec,
        )

        if not _passes_only_filter(spec.domain, spec.scenario_id, spec.scenario_name, only_set):
            skipped.append({"domain": spec.domain, "scenario_id": spec.scenario_id, "reason": "only_filter"})
            continue

        fixture_payload, fixture_name = _load_fixture_override(spec, input_dir, base_for_spec)

        for level in chosen_levels:
            client_variant = _build_canonical_seed_payload(
                base_client or base_eval,
                spec=spec,
                fixture_payload=fixture_payload,
                fixture_name=fixture_name,
                level=level,
                payload_kind="client_bundle",
            )
            eval_variant = _build_canonical_seed_payload(
                base_eval or base_client,
                spec=spec,
                fixture_payload=fixture_payload,
                fixture_name=fixture_name,
                level=level,
                payload_kind="openenv_eval",
            )
            realigned_count += 2

            stem = f"{_slugify(spec.domain)}__{_slugify(spec.scenario_id)}__{level.name}"
            client_name = f"{stem}.client_bundle.json"
            eval_name = f"{stem}.openenv_eval.json"
            _dump_json(output_dir / client_name, client_variant)
            _dump_json(output_dir / eval_name, eval_variant)
            curriculum_clients.append(client_variant)
            curriculum_evals.append(eval_variant)
            written_files.extend([client_name, eval_name])

    if not curriculum_clients:
        raise CurriculumBuildError("no payloads matched the requested filters")

    all_clients_name = "all_client_curriculum_bundles.json"
    all_evals_name = "all_openenv_curriculum_payloads.json"
    index_name = "curriculum_index.json"
    domain_counts: dict[str, int] = {}
    for item in curriculum_clients:
        domain_counts[str(item.get("domain") or "general")] = domain_counts.get(str(item.get("domain") or "general"), 0) + 1

    summary = {
        "ok": True,
        "input_dir": str(input_dir),
        "output_dir": str(output_dir),
        "source": spec_source,
        "expected_domain_count": EXPECTED_DOMAIN_COUNT,
        "domain_count": len(domain_counts),
        "count": len(curriculum_clients),
        "eval_count": len(curriculum_evals),
        "levels": [level.name for level in chosen_levels],
        "realigned_count": realigned_count,
        "domain_counts": dict(sorted(domain_counts.items())),
        "warnings": warnings,
        "skipped": skipped,
        "registry": registry_report,
        "files": written_files + [all_clients_name, all_evals_name, index_name],
    }
    _dump_json(output_dir / all_clients_name, curriculum_clients)
    _dump_json(output_dir / all_evals_name, curriculum_evals)
    _dump_json(output_dir / index_name, summary)
    return summary


def main(argv: Sequence[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Build curriculum payload variants from generated OpenEnv payloads.")
    parser.add_argument("--input-dir", help="Directory containing all_client_bundles.json and all_openenv_eval_payloads.json")
    parser.add_argument("--output-dir", default=str(DEFAULT_OUTPUT_DIR), help="Directory where curriculum payloads will be written")
    parser.add_argument("--levels", nargs="*", help="Optional subset of curriculum levels to generate")
    parser.add_argument("--only", nargs="*", help="Restrict generation to one or more domains, scenario IDs, or scenario names")
    parser.add_argument("--source", choices=("auto", "registry", "fallback"), default="auto", help="Metadata source for canonical scenario specs")
    parser.add_argument("--validate-registry-imports", action="store_true", help="Run validate_registry(import_all=True) when registry metadata is available")
    parser.add_argument("--json", action="store_true", help="Print a JSON summary")
    args = parser.parse_args(list(argv) if argv is not None else None)

    try:
        report = build_curriculum_payloads(
            input_dir=_resolve_input_dir(args.input_dir),
            output_dir=Path(args.output_dir).resolve(),
            levels=args.levels,
            only=args.only,
            source=args.source,
            validate_registry_imports=args.validate_registry_imports,
        )
    except CurriculumBuildError as exc:
        report = {"ok": False, "error": str(exc), "type": "contract_error"}
        if args.json:
            print(json.dumps(report, indent=2, ensure_ascii=False))
        else:
            print(f"[fail] {report['error']}")
        return 1

    if args.json:
        print(json.dumps(report, indent=2, ensure_ascii=False))
    else:
        print("[ok] curriculum payloads generated")
        print(f"- input_dir: {report['input_dir']}")
        print(f"- output_dir: {report['output_dir']}")
        print(f"- source: {report['source']}")
        print(f"- domains: {report['domain_count']}/{report['expected_domain_count']}")
        print(f"- count: {report['count']}")
        print(f"- realigned_count: {report['realigned_count']}")
        print(f"- levels: {', '.join(report['levels'])}")
        if report.get("warnings"):
            print("- warnings:")
            for warning in report["warnings"]:
                print(f"  - {warning}")
        for name in report["files"]:
            print(f"- {name}")
    return 0


